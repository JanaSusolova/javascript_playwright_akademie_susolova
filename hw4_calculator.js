function secist(a, b) {
  const result = a + b;
  console.log("Výsledek sčítání: " + result);
}

function odecist(a, b) {
  const result = a - b;
  console.log("Výsledek odečítání: " + result);
}

function vynasobit(a, b) {
  const result = a * b;
  console.log("Výsledek násobení: " + result);
}

function vydelit(a, b) {
  const result = a / b;
  console.log("Výsledek dělení: " + result);
}

secist(565, 584);
odecist(474, 520);
vynasobit(5822, 65);
vydelit(632, 6);
