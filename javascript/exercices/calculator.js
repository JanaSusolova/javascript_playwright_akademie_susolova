function add(a, b) {
  const result = a + b;
  console.log("Výsledek sčítání: " + result);
}

function subtract(a, b) {
  const result = a - b;
  console.log("Výsledek odečítání: " + result);
}

function multiply(a, b) {
  const result = a * b;
  console.log("Výsledek násobení: " + result);
}

function divide(a, b) {
  const result = a / b;
  console.log("Výsledek dělení: " + result);
}

add(15, 48);
subtract(98, 41);
multiply(30, 65);
divide(60, 5);
